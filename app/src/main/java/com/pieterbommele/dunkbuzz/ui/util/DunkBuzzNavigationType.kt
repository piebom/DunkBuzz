package com.pieterbommele.dunkbuzz.ui.util

/**
 * Enum class representing different types of navigation patterns used in the DunkBuzz application.
 */
enum class DunkBuzzNavigationType {
    /**
     * Bottom Navigation: Navigation using tabs at the bottom of the screen.
     */
    BOTTOM_NAVIGATION,

    /**
     * Navigation Rail: Navigation using a rail or bar typically placed on the left or right side of the screen.
     */
    NAVIGATION_RAIL,

    /**
     * Permanent Navigation Drawer: Navigation using a permanent navigation drawer typically placed on one side of the screen.
     */
    PERMANENT_NAVIGATION_DRAWER
}
